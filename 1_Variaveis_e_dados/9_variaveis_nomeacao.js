/*
    Nomes de variáveis

    JS é case-sensitive, isto quer dizer que letras maiúsculas e minúsculas fazem diferença
    JS aceita a cadeia de caracteres Unicode, isto é, podemos colocar aspas e acentuações
    
    O que é ideal:
        Criar nomes que fazem sentido com a situação, que explique o que a variável é ou faz
        Que sejam em inglês
        camelCase - nomes com mais de uma palavra, da segunda em diante tem sua primeira letra em maiúculo
        snake_case - para separar palavras também dá para usar _

    O que é permitido fazer:
        Iniciar com os caracteres especiais $ e _
        Iniciar com letras
        Colocar acentos
        Letras maiúsculas e minúsculas fazem diferença
    
    O que não é permitido fazer:
        Iniciar com números
        Colocar espaços vazios no nome (nome Da Variavel)
*/