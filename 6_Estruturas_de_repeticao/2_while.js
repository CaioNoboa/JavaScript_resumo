/*
    While - estrutura de repetição

    Estrutura
    while(condição) {
        Bloco de códigos
        Atualização da variável da condição
    }
    Enquanto a condição for true rodará o bloco de códigos

    While vs for
        Preferimos usar while quando não sabemos qual é o momento da parada da condição
*/

let i = 0;

while(i<10) {
    console.log(i);
    i++;
}

