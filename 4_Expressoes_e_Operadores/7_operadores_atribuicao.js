/*
    Operadores de atribuição (Assignment)
    =
    +=
    -=
    *=
    /=
    %=
    **=
*/

// assignment
x = 1;

// addition assignment
y = 1;
y += 1; // é o mesmo que y = y + 1
console.log(y);

// subtraction assignment
w = 1;
w -= 1; // é o mesmo que w = w - 1
console.log(w);

// multiplication assignment
z = 5;
z *= 10; // é o mesmo que z = z * 10
console.log(z);

// subtraction assignment
v = 10;
v /= 2; // é o mesmo que v = v / 2
console.log(v);

// remainder
a = 9;
a %= 2 // é o mesmo que a = a % 2
console.log(`O resto da divisão de 9 por 2 é ${a}`);

// exponentiation
x = 2;
x **= 4 // é o mesmo que x = x ** 4 (x elevado à quarta potência)
console.log(x);